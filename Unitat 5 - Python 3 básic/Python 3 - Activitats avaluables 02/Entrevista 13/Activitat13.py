import requests

species = input("Type species: ") # We are asking the user to type what type of species they want
URL = 'https://rickandmortyapi.com/api/character/?species=' + species # Here we are generating the link

while (URL != None): # We are creating this loop so we can get all the characters, even those in other pages
    response = requests.get(URL) 
    print(response)
    data = response.json() # Fetching the json from the response
    info = data['info']
    results = data['results']
    for element in results: # We are printing the names
        print(element['name'])   
    URL = info['next'];
    
